import { useState, useMemo, ReactNode } from 'react'
import {
  Table,
  TableBody,
  TableCell,
  TableContainer,
  TableHead,
  TableRow,
  TablePagination,
  TableSortLabel,
  Paper,
  IconButton,
  Tooltip,
  Box,
  TextField,
  InputAdornment,
  Menu,
  MenuItem,
  LinearProgress
} from '@mui/material'
import DownloadIcon from '@mui/icons-material/Download'
import SearchIcon from '@mui/icons-material/Search'

interface Column {
  id: string
  label: string
  sortable?: boolean
  filterable?: boolean
  format?: (value: any, row?: any) => ReactNode
}

interface DataTableProps {
  columns: Column[]
  rows: any[]
  searchable?: boolean
  loading?: boolean
  exportFilename?: string
  exportMetadata?: {
    dateRange?: string
    filters?: string[]
    generatedBy?: string
  }
}

export default function DataTable({ columns, rows, searchable = false, loading = false, exportFilename = 'data', exportMetadata }: DataTableProps) {
  const [page, setPage] = useState(0)
  const [rowsPerPage, setRowsPerPage] = useState(10)
  const [orderBy, setOrderBy] = useState<string>('')
  const [order, setOrder] = useState<'asc' | 'desc'>('asc')
  const [searchTerm, setSearchTerm] = useState('')
  const [anchorEl, setAnchorEl] = useState<null | HTMLElement>(null)

  const handleSort = (columnId: string) => {
    const isAsc = orderBy === columnId && order === 'asc'
    setOrder(isAsc ? 'desc' : 'asc')
    setOrderBy(columnId)
  }

  // Filter rows based on search term — memoized to avoid O(n×m) on every render
  const filteredRows = useMemo(() =>
    searchTerm
      ? rows.filter((row) =>
          columns.some((column) => {
            const value = row[column.id]
            return value?.toString().toLowerCase().includes(searchTerm.toLowerCase())
          })
        )
      : rows,
    [rows, columns, searchTerm]
  )

  const sortedRows = useMemo(() =>
    [...filteredRows].sort((a, b) => {
      if (!orderBy) return 0
      const aVal = a[orderBy]
      const bVal = b[orderBy]
      if (aVal < bVal) return order === 'asc' ? -1 : 1
      if (aVal > bVal) return order === 'asc' ? 1 : -1
      return 0
    }),
    [filteredRows, orderBy, order]
  )

  const paginatedRows = useMemo(() =>
    sortedRows.slice(page * rowsPerPage, page * rowsPerPage + rowsPerPage),
    [sortedRows, page, rowsPerPage]
  )

  const handleExportClick = (event: React.MouseEvent<HTMLElement>) => {
    setAnchorEl(event.currentTarget)
  }

  const handleClose = () => {
    setAnchorEl(null)
  }

  const exportToCSV = () => {
    // Create metadata rows
    const metadataRows: string[] = []
    if (exportMetadata) {
      metadataRows.push(`# Export Metadata`)
      metadataRows.push(`# Generated: ${new Date().toLocaleString()}`)
      if (exportMetadata.dateRange) {
        metadataRows.push(`# Date Range: ${exportMetadata.dateRange}`)
      }
      if (exportMetadata.filters && exportMetadata.filters.length > 0) {
        metadataRows.push(`# Filters: ${exportMetadata.filters.join(', ')}`)
      }
      if (exportMetadata.generatedBy) {
        metadataRows.push(`# Generated By: ${exportMetadata.generatedBy}`)
      }
      metadataRows.push(`# Total Records: ${sortedRows.length}`)
      metadataRows.push('') // Empty line
    }

    // Create CSV content
    const headers = columns.map(col => col.label).join(',')
    const csvRows = sortedRows.map(row =>
      columns.map(col => {
        const value = col.format ? col.format(row[col.id]) : row[col.id]
        // Escape quotes and wrap in quotes if contains comma
        const stringValue = String(value || '')
        return stringValue.includes(',') ? `"${stringValue.replace(/"/g, '""')}"` : stringValue
      }).join(',')
    )

    const csvContent = [...metadataRows, headers, ...csvRows].join('\n')

    // Download
    const blob = new Blob([csvContent], { type: 'text/csv;charset=utf-8;' })
    const link = document.createElement('a')
    link.href = URL.createObjectURL(blob)
    link.download = `${exportFilename}_${new Date().toISOString().split('T')[0]}.csv`
    link.click()
    handleClose()
  }

  const exportToExcel = () => {
    // Create metadata section
    let metadataHtml = ''
    if (exportMetadata) {
      metadataHtml = `
        <table border="0" style="margin-bottom: 20px;">
          <tr><td colspan="2"><strong>Export Metadata</strong></td></tr>
          <tr><td>Generated:</td><td>${new Date().toLocaleString()}</td></tr>
          ${exportMetadata.dateRange ? `<tr><td>Date Range:</td><td>${exportMetadata.dateRange}</td></tr>` : ''}
          ${exportMetadata.filters && exportMetadata.filters.length > 0 ? `<tr><td>Filters:</td><td>${exportMetadata.filters.join(', ')}</td></tr>` : ''}
          ${exportMetadata.generatedBy ? `<tr><td>Generated By:</td><td>${exportMetadata.generatedBy}</td></tr>` : ''}
          <tr><td>Total Records:</td><td>${sortedRows.length}</td></tr>
        </table>
      `
    }

    // Create HTML table for Excel
    const headers = columns.map(col => `<th>${col.label}</th>`).join('')
    const rows = sortedRows.map(row =>
      `<tr>${columns.map(col => {
        const value = col.format ? col.format(row[col.id]) : row[col.id]
        return `<td>${value || ''}</td>`
      }).join('')}</tr>`
    ).join('')

    const htmlContent = `
      <html xmlns:o="urn:schemas-microsoft-com:office:office" xmlns:x="urn:schemas-microsoft-com:office:excel">
        <head><meta charset="UTF-8"></head>
        <body>
          ${metadataHtml}
          <table border="1">
            <thead><tr>${headers}</tr></thead>
            <tbody>${rows}</tbody>
          </table>
        </body>
      </html>
    `

    // Download
    const blob = new Blob([htmlContent], { type: 'application/vnd.ms-excel' })
    const link = document.createElement('a')
    link.href = URL.createObjectURL(blob)
    link.download = `${exportFilename}_${new Date().toISOString().split('T')[0]}.xls`
    link.click()
    handleClose()
  }

  return (
    <Paper>
      <Box sx={{ p: 2, display: 'flex', justifyContent: 'space-between', alignItems: 'center' }}>
        {searchable && (
          <TextField
            size="small"
            placeholder="Search..."
            value={searchTerm}
            onChange={(e) => {
              setSearchTerm(e.target.value)
              setPage(0) // Reset to first page on search
            }}
            InputProps={{
              startAdornment: (
                <InputAdornment position="start">
                  <SearchIcon />
                </InputAdornment>
              ),
            }}
            sx={{ minWidth: 250 }}
          />
        )}
        <Box sx={{ flexGrow: 1 }} />
        <Tooltip title="Export Data">
          <IconButton onClick={handleExportClick}>
            <DownloadIcon />
          </IconButton>
        </Tooltip>
        <Menu
          anchorEl={anchorEl}
          open={Boolean(anchorEl)}
          onClose={handleClose}
        >
          <MenuItem key="csv" onClick={exportToCSV}>Export as CSV</MenuItem>
          <MenuItem key="excel" onClick={exportToExcel}>Export as Excel</MenuItem>
        </Menu>
      </Box>
      <TableContainer>
        <Table>
          <TableHead>
            <TableRow>
              {columns.map((column) => (
                <TableCell key={column.id}>
                  {column.sortable !== false ? (
                    <TableSortLabel
                      active={orderBy === column.id}
                      direction={orderBy === column.id ? order : 'asc'}
                      onClick={() => handleSort(column.id)}
                    >
                      {column.label}
                    </TableSortLabel>
                  ) : (
                    column.label
                  )}
                </TableCell>
              ))}
            </TableRow>
          </TableHead>
          <TableBody>
            {loading && (
              <TableRow>
                <TableCell colSpan={columns.length} sx={{ p: 0 }}>
                  <LinearProgress />
                </TableCell>
              </TableRow>
            )}
            {paginatedRows.map((row, index) => (
              <TableRow key={row.id || index} hover>
                {columns.map((column) => (
                  <TableCell key={column.id}>
                    {column.format
                      ? column.format(row[column.id], row)
                      : row[column.id]}
                  </TableCell>
                ))}
              </TableRow>
            ))}
          </TableBody>
        </Table>
      </TableContainer>
      <TablePagination
        rowsPerPageOptions={[5, 10, 25, 50]}
        component="div"
        count={filteredRows.length}
        rowsPerPage={rowsPerPage}
        page={page}
        onPageChange={(_, newPage) => setPage(newPage)}
        onRowsPerPageChange={(e) => {
          setRowsPerPage(parseInt(e.target.value, 10))
          setPage(0)
        }}
      />
    </Paper>
  )
}
